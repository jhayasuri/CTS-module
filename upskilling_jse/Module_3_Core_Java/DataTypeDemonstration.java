class DataTypeDemonstration{
    public static void main(String[] args) {
        int age = 21;
        float height = 5.9f;
        double weight = 66.5;
        char grade = 'A';
        boolean isPassed = true;
        System.out.println("Integer (int): " + age);
        System.out.println("Float (float): " + height);
        System.out.println("Double (double): " + weight);
        System.out.println("Character (char): " + grade);
        System.out.println("Boolean (boolean): " + isPassed);
    }
}
